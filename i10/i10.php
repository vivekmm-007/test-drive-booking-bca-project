<div class="summary" ><h2>About Hyundai Grand i10 Nios</h2><div class="clr"></div><div class="box_new min_synopsis" id="synopsis-content" >
    <p><strong>Model Launch Date:</strong></p><p>The updated <a href='grand-i10.html' title='Hyundai Grand i10 Nios'>Grand i10 Nios</a> was launched in India on 1 April, 2023.</p><p><strong>Feature list:</strong></p><p><strong>Exterior</strong>:</p><p>On the outside, the Grand i10 Nios facelift now gets a freshly designed front grille, new body-coloured front and rear bumpers, LED DRLs integrated into the front bumper, a shark-fin antenna, new LED taillamps, and projector headlamps. The car rides on a new set of 15-inch diamond-cut alloy wheels.</p><p>The hatchback is offered in six monotone colour options, including Polar White, Titan Grey, Typhoon Silver, Teal Blue, Fiery Red, and a new exclusive colour – Spark Green. It is also offered in two dual-tone options – Spark Green and Polar White with a black roof.</p><p><strong>Interior</strong>:</p><p>Inside the cabin, the Grand i10 Nios facelift comes equipped with features such as an eight-inch touchscreen display with Apple CarPlay and Android Auto, cruise control, fully automatic temperature control, voice recognition, and a wireless phone charger. It is a five-seater hatchback.</p><p><strong>Engine, Performance, and Specifications:</strong></p><p>Under the hood, the Grand i10 Nios is powered by a 1.2-litre Kappa gasoline engine that produces 82bhp and 113.8Nm of torque, mated with a five-speed manual unit and an AMT unit. On the other hand, it is also offered with a 1.2-litre Kappa Petrol-CNG engine that belts out 68bhp and 95.2Nm of torque paired only with a five-speed manual gearbox. Both engines are now RDE and BS6 Phase 2-compliant.</p><p><strong>Variants</strong>:</p><p>The hatchback is offered in four variants, namely Era, Magna, Sportz, and Asta.</p><p><strong>Safety</strong>:</p><p> Hyundai has not tested the Grand i10 Nios for any safety ratings yet.</p>

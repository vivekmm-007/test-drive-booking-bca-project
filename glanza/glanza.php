<h2>Introduction</h2>

<p>The new Toyota Glanza was launched in India on 15 March, 2022. Toyota Glanza is available in four petrol variants including E, S, G, V, and two CNG variants - S and G. The CNG variants were launched on 9 November, 2022.</p>

<h3>Exterior</h3>

<p>The new Glanzaâ€™s design highlights include LED projector headlamps, new L-shaped daytime running lights, redesigned grille and bumpers, reshaped taillights, and new alloy wheels. Toyota Glanza is offered in five colours, namely Insta Blue, Gaming Grey, Enticing Silver, Sportin Red, and Cafe White.</p>

<h3>Interior</h3>

<p>The interior features of the Glanza include a Heads-Up Display (HUD), a 360-degree camera, a nine-inch touchscreen infotainment system with Apple CarPlay and Android Auto, cruise control, automatic climate control, a dual-tone dashboard, rear AC vents, a height-adjustable driver seat, and an Arkamys-sourced music system. On the safety front, it is equipped with six airbags, ABS with EBD, ESP, rear parking sensors, a speed alert system, and a seat-belt reminder system.</p>

<h3>Engine and Transmission</h3>

<p>The new Toyota Glanza is powered by a 1.2-litre, four-cylinder, dual VVT petrol engine producing 89bhp and 113Nm of torque. The E-CNG engine produces 76bhp and 98.5Nm of torque and returns a fuel efficiency of 30.61km/kg. While the petrol engine is paired with a five-speed manual transmission and an AMT unit, the E-CNG variant only has a five-speed manual transmission.</p>

<div class="summary" ><h2 style="font-size: 24px; color: #ffffff;">About Maruti Alto 800</h2><div class="clr"></div><div class="box_new min_synopsis" id="synopsis-content" >
    <p style="font-size: 20px; color: #ffffff;"><strong>Summary</strong></p>
<p style="color: #ffffff;">Maruti Suzuki&rsquo;s longest-running brand name got a major update a few years ago and is now only referred to as the Alto. The entry-level hatchback can be had in three variants with a BSVI compliant 799cc engine as well as an updated set of standard safety features. The Japanese automaker offers the car in six colour options that include the likes of Uptown Red, Superior White, Silky Silver, Granite Grey, Mojito Green, and Cerulean Blue.</p>
<p style="font-size: 20px; color: #ffffff;"><strong>Exterior</strong></p>
<p style="color: #ffffff;">Maruti Suzuki has retained the iconic appearance of the Alto range but has added a new front grille, a revised headlamp and redesigned bumpers. In the profile, you can see that the automaker has also tweaked the side fender as a part of the updates. The company claims that the new model complies with the upcoming crash and pedestrian safety regulation.</p>
<p style="font-size: 20px; color: #ffffff;"><strong>Interior</strong></p>
<p style="color: #ffffff;">In terms of interior design, the cabin has been fitted with a dual-tone finish with new air-con ducts and a stylish speedometer. The new front and rear bottle holders also add practicality to the updated interiors. Moreover, the new Alto gets the Smart Play Dock with Bluetooth connectivity. This Smart Play Dock transforms the phone into a touch interface to access calls, music and navigation.</p>
<p style=" font-size: 20px; color: #ffffff;"><strong>Engine</strong></p>
<p style="color: #ffffff;">Powering the Alto is a BS6 compliant 796cc petrol engine producing 48bhp and 69Nm. It has got upgraded hardware and software along with a reworked exhaust system. The claimed fuel efficiency of the new Alto is 22.05kmpl. Standard safety features now include - reverse parking sensor, driver airbag, speed alert system and seat belt reminder for both driver and the co-driver, and ABS with EBD.&nbsp;</p>

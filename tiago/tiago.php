<h2>About Tata Tiago</h2>

<p style="font-size: 24px; color: rgb(0, 0, 0);"><strong>Model launch date:</strong></p>
<p style="font-size: 20px; color: rgb(25, 0, 255);">The 2023 Tata Tiago was launched on 11 February, 2023.</p>

<p style="font-size: 24px; color: rgb(0, 0, 0);"><strong>Feature list:</strong></p>

<p style="font-size: 24px; color: rgb(0, 0, 0);"><strong>Exterior:</strong></p>
<p style="font-size: 20px; color: rgb(43, 0, 255);">The updated Tiago features a new grille and a chrome strip, a tweaked bumper with air dams and circular fog lamps, and dual-tone alloy wheels in higher variants along with black highlights. Overall, the vehicle has retained its familiar design elements except for cosmetic enhancements for freshness.</p>

<p style="font-size: 24px; color: rgb(0, 0, 0);"><strong>Interior:</strong></p>
<p style="font-size: 20px; color: rgb(0, 8, 255);">The interior gets mild updates over its predecessor. One of the noticeable changes includes a touchscreen infotainment system with Android Auto and Apple Carplay. In terms of safety, the standard equipment list includes - dual airbags, ABS, EBD, rear parking sensors, a seat-belt reminder, and a high-speed alert system. The Tiago can accommodate five passengers.</p>

<p style="font-size: 24px; color: rgb(0, 0, 0);"><strong>Engine, performance, and specifications:</strong></p>
<p style="font-size: 20px; color: rgb(30, 0, 255);">Under the hood, the Tiago is powered by a BS6 2-compliant 1.2-litre, three-cylinder petrol engine that generates 84bhp and 113Nm of torque. The engine can be had in both five-speed manual and five-speed AMT options. Meanwhile, the CNG version of the Tiago is tuned to produce 72bhp and 95Nm of peak torque and is available only with a manual gearbox.</p>

<p style="font-size: 24px; color: rgb(0, 0, 0);"><strong>Variants:</strong></p>

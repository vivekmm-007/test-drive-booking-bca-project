<h2>About Tata Punch</h2>

<p>Tata has entered the B-segment mini SUV game with another player. It’s the production-ready model of the HBX concept and is officially called the Punch.</p>

<p>It is underpinned by the same ALFA platform as the Altroz but will have a smaller length that is expected to be around 3850mm, the segment standard. In front, you get a face similar to the Harrier SUV but with sleek LEDs on the top and a tri-arrow design where the headlamps will be placed. It’s expected to look larger than life thanks to an aggressive body cladding and 15-inch wheels with a diamond-cut design.</p>

<p>The fully loaded Punch will get a segment standard feature list that comprises button start, cruise control, climate control, semi-digital instrument cluster, touchscreen infotainment system, and a height adjustment mechanism for the driver’s seat.</p>

<p>Tata has long since exited the diesel segment in this part of the market, and so the Punch only gets their 1.2-litre petrol engine that produces 84bhp/113Nm. It will be offered with either a five-speed manual or a five-speed automated manual.</p>

<p>The Punch is Tata’s competitor in the newly formed micro SUV segment and rivals against the likes of Ford Freestyle, Mahindra KUV100, and the Maruti Suzuki Ignis.</p>

<h2>Model launch date:</h2>

<p>The Toyota Innova Hycross was launched in India on 28 December, 2022.</p>

<h2>Feature list:</h2>

<p>Compared to the Crysta, the Hycross is a newer model that boasts a more muscular appearance with a large trapezoidal grille at the front, flanked by slim LED headlights. Its front bumper gets triangular vents with thin LED daytime running lights, while the rear sports wraparound taillights, connected by a chrome strip.</p>

<p>Inside the MPV's cabin, the dual-tone interior is the same whether one opts for the seven- or eight-seater version. The highlights include a floating 10.1-inch touchscreen infotainment system, semi-digital instrument cluster, three-spoke steering wheel, gear shifter, and an electronic parking brake.</p>

<h2>Engine, performance, and specifications:</h2>

<p>The Innova Hycross is offered with two powertrain options, including a pure petrol engine and a petrol-hybrid system. Both these units come mated to a CVT unit that sends power to the front wheels. The 2.0-litre petrol mill churns out 172bhp and 197Nm of torque. On the other hand, the petrol-hybrid system uses the same 2.0-litre petrol unit but gets Toyota's fifth-generation strong hybrid technology for a combined output of 183bhp. Its claimed mileage is 21.1kmpl.</p>

<h2>Variants:</h2>

<p>The Hycross is available in G (7S), G (8S), GX (7S), and GX (8S) trims for the petrol-only model. On the other hand, the hybrid versions are available in VX (7S), VX (8S), ZX, and ZX (O) variants.</p>

<h2>Safety:</h2>

<p>The Toyota Hycross has not been tested yet by GNCAP for safety ratings.</p>

<h2>Competition:</h2>

<p>The Toyota Hycross competes with the Mahindra XUV 700, Tata Safari, Toyota Innova Crysta, and the Mahindra Scorpio N.</p>

<p>Last updated on 26 October, 2023.</p>

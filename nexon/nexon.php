<h2 style="font-size: 24px; color: #ffffff;">About Tata Nexon [-] [2020-2023]</h2>

    <p style="font-size: 18px; color: #ffffff;">The Tata Nexon, a compact SUV, combines affordability and performance seamlessly. Priced between 7.0 Lakhs and 13.24 Lakhs (Ex-Showroom), it offers a feature-packed driving experience in the competitive compact SUV segment.</p>

    <h3  style="font-size: 24px; color: #ffffff;">Price:</h3>
    <p style="font-size: 18px; color: #ffffff;">Starting at 7.0 Lakhs: The base variant of the Tata Nexon is attractively priced, making it an accessible choice for a wide range of buyers.</p>
    <p style="font-size: 18px; color: #ffffff;">Up to 13.24 Lakhs: The top-tier models, although priced higher, provide a comprehensive set of features and capabilities.</p>

    <h3  style="font-size: 24px; color: #ffffff;">Specs and Features:</h3>
    <ul>
        <li  style="font-size: 18px; color: #ffffff;"><strong>Engine Variety:</strong> The Tata Nexon offers engine options ranging from 1199.0cc to 1497.0cc, accommodating diverse driving preferences.</li>
        <li style="font-size: 18px; color: #ffffff;"><strong>Mileage:</strong> With a mileage range of 17.05 to 24.07 miles per gallon, it caters to both efficiency-conscious drivers and those looking for more power.</li>
        <li style="font-size: 18px; color: #ffffff;"><strong>Transmission Choices:</strong> Buyers can opt for either manual or automatic transmissions, allowing for a personalized driving experience.</li>
        <li style="font-size: 18px; color: #ffffff;"><strong>Safety:</strong> Equipped with features like Antilock Braking System (ABS), Airbags, and Parking Sensors, the Nexon prioritizes safety on the road.</li>
        <li style="font-size: 18px; color: #ffffff;"><strong>Comfort and Convenience:</strong> The Nexon offers rear AC vents, cruise control, alloy wheels, keyless entry, automatic climate control, and a sunroof for added comfort and convenience.</li>
        <li style="font-size: 18px; color: #ffffff;"><strong>Turbo Engine:</strong> With a turbocharged engine option, it provides an extra boost of power for those seeking a spirited driving experience.</li>
    </ul>

    <h3  style="font-size: 24px; color: #ffffff;">Rivals:</h3>
    <p style="font-size: 18px; color: #ffffff;">Kia Sonet, Renault Kiger, Mahindra Bolero Neo, Tata Punch CNG, Mahindra XUV300 Turbo Sport: These are the primary competitors to the Tata Nexon, each with its unique features and offerings.</p>

    <p style="font-size: 18px; color: #ffffff;">The Tata Nexon excels as a value-packed compact SUV. Its range of engines, transmission choices, and safety features make it a versatile choice for various driving needs. With an attractive starting price, it offers affordability without compromising on quality and performance. When compared to its rivals, the Nexon stands out as a well-rounded and reliable option, making it a strong contender in the compact SUV market.</p>
